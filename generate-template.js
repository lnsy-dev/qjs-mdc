#!/usr/bin/env qjs
/**
 * @fileoverview Generate notebook template bundle from test-notebook directory
 */

import * as std from 'std';
import * as os from 'os';

const SOURCE_DIR = 'test-notebook';
const OUTPUT_FILE = 'src/templates/notebook-template.js';

function escapeForJS(str) {
  return JSON.stringify(str);
}

function findFiles(dir, prefix = '') {
  const files = [];
  const [entries, err] = os.readdir(dir);
  
  if (err) {
    console.error(`Error reading ${dir}:`, err);
    return files;
  }
  
  for (const entry of entries) {
    const fullPath = `${dir}/${entry}`;
    const relativePath = prefix ? `${prefix}/${entry}` : entry;
    const [st, stErr] = os.stat(fullPath);
    
    if (stErr) continue;
    
    if (st.mode & os.S_IFDIR) {
      // Directory - recurse
      files.push(...findFiles(fullPath, relativePath));
    } else if (st.mode & os.S_IFREG) {
      // Regular file
      files.push({ path: fullPath, relative: relativePath });
    }
  }
  
  return files;
}

function main() {
  console.log(`Generating notebook template from ${SOURCE_DIR}...`);
  
  // Find all files
  const files = findFiles(SOURCE_DIR);
  files.sort((a, b) => a.relative.localeCompare(b.relative));
  
  console.log(`Found ${files.length} files`);
  
  // Start output
  let output = `/**
 * @fileoverview Bundled notebook template for scaffolding new projects.
 * Generated by generate-template.js - DO NOT EDIT MANUALLY
 */

export const NOTEBOOK_TEMPLATE = {
`;
  
  // Process each file
  for (const file of files) {
    const f = std.open(file.path, 'r');
    if (!f) {
      console.error(`Cannot read ${file.path}`);
      continue;
    }
    
    let content = '';
    let line;
    while ((line = f.getline()) !== null) {
      content += line + '\n';
    }
    // Remove trailing newline
    if (content.endsWith('\n')) {
      content = content.slice(0, -1);
    }
    f.close();
    
    output += `  ${escapeForJS(file.relative)}: ${escapeForJS(content)},\n`;
  }
  
  output += '};\n';
  
  // Write output
  const outFile = std.open(OUTPUT_FILE, 'w');
  outFile.puts(output);
  outFile.close();
  
  console.log(`✓ Generated ${OUTPUT_FILE}`);
  console.log(`✓ Template contains ${files.length} files`);
}

main();
